package com.jungseokyung.makepost.model;

public class FileInfo {
	String delim;
	String filepath;
	String file_charset;
	String column_header;
	
	
	public String getDelim() {
		return delim;
	}
	public void setDelim(String delim) {
		this.delim = delim;
	}
	public String getFilepath() {
		return filepath;
	}
	public void setFilepath(String filepath) {
		this.filepath = filepath;
	}
	public String getFile_charset() {
		return file_charset;
	}
	public void setFile_charset(String file_charset) {
		this.file_charset = file_charset;
	}
	public String getColumn_header() {
		return column_header;
	}
	public void setColumn_header(String column_header) {
		this.column_header = column_header;
	}
	
	
	
}
